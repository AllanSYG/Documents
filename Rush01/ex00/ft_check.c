void	check_same(int pos, char **result , char n)
{
	int	i;
	i = 0;
	while (i < 4)
	{
		if (result[pos / 4][i] == n)
			return (1);
		i++;
	}
	i = 0;
	while (i < 4)
	{
		if (result[i][pos % 4] == n)
			return (1);
		i++;
	}
	return (0);
}
