void	result1(char **result)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (j < 4)
	{
		while (i < 4)
		{
			ft_putchar(result[j][i]);
			i++;
		}
		ft_putchar('\n');
		j++;
	}
}
