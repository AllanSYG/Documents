#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;

	j = 1;
	i = 0;
	while (j != ac)
	{
		while (av[j][i])
		{
			write(1, &av[j][i], 1);
			i++;
		}
		i = 0;
		j++;
		write(1, "\n", 1);
	}
	return (0);
}
